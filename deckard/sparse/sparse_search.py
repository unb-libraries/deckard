
class SparseSearch:
    """Provides a base class for sparse search databases."""
    def __init__(self, uri, log):
        pass

    def flush_data(self):
        """Flushes the data from the sparse search endpoint."""

    def search(self, query):
        """Searches the sparse search endpoint for the query."""

    def index_document(self, document):
        """Adds document to the sparse search endpoint."""
